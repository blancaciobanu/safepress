export const allTasks = Object.entries(setupTasks).flatMap(([key, cat]) =>
  cat.tasks.map(t => ({ ...t, categoryKey: key }))
);

export const TASKS_BY_ID = Object.fromEntries(allTasks.map((task) => [task.id, task]));

export const DEFAULT_TASK_ORDER = [...allTasks]
  .sort((a, b) => {
    const priorityDelta = (PRIORITY_ORDER[a.priority] ?? 3) - (PRIORITY_ORDER[b.priority] ?? 3);
    if (priorityDelta !== 0) return priorityDelta;
    return a.title.localeCompare(b.title);
  })
  .map((task) => task.id);
